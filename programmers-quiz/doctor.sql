다음은 종합병원에 속한 의사 정보를 담은DOCTOR 테이블입니다. DOCTOR 테이블은 다음과 같으며 
DR_NAME, DR_ID, LCNS_NO, HIRE_YMD, MCDP_CD, TLNO는 각각 의사이름, 의사ID, 면허번호, 고용일자, 진료과코드, 전화번호를 나타냅니다.

Column name Type  Nullable
DR_NAME VARCHAR(20) FALSE
DR_ID VARCHAR(10) FALSE
LCNS_NO VARCHAR(30) FALSE
HIRE_YMD  DATE  FALSE
MCDP_CD VARCHAR(6)  TRUE
TLNO  VARCHAR(50) TRUE

CREATE TABLE IF NOT EXISTS DOCTOR (
  DR_NAME VARCHAR(20) NOT NULL,
  DR_ID VARCHAR(10) NOT NULL PRIMARY KEY,
  LCNS_NO VARCHAR(30) NOT NULL,
  HIRE_YMD DATE NOT NULL,
  MCDP_CD VARCHAR(6),
  TLNO  VARCHAR(50)
);

