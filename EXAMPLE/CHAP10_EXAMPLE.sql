--EX05 급여등급 5등급 사원 삭제
DELETE FROM CHAP10HW_EMP
WHERE EMPNO = ANY (SELECT E.EMPNO
    FROM CHAP10HW_EMP E
    JOIN CHAP10HW_SALGRADE S
    ON E.SAL BETWEEN S.LOSAL AND S.HISAL
    WHERE S.GRADE=5);