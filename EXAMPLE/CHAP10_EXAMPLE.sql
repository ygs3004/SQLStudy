--EX04 CHAP10HW_EMP 60번 부서의 사원중 입사일이 가장 빠른 사원보다 늦게 입사한 사원의 급여 10% 인상, 80번 부서로 이동
UPDATE CHAP10HW_EMP 
    SET SAL = SAL*1.1,
         DEPTNO = 80
    WHERE HIREDATE > (SELECT MIN(HIREDATE) FROM CHAP10HW_EMP WHERE DEPTNO=60);
        
